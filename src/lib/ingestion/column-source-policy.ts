export function getEnabledSourcesForColumn(column: IngestionColumn, requestedTiers?: readonly (SourceTier | string)[]): ColumnSourceConfig[] {
  const tierSet = requestedTiers?.length ? new Set(requestedTiers) : undefined;
  return columnSourceConfigs.filter((source) => source.column === column && (source.enabled || tierSet?.has(source.source_tier)) && (!tierSet || tierSet.has(source.source_tier)));
}

export interface ColumnSourcePlan {
  columns: IngestionColumn[];
  sources: ColumnSourceConfig[];
}

export function createColumnSourcePlan(requestedColumns?: readonly (IngestionColumn | string)[], requestedTiers?: readonly (SourceTier | string)[]): ColumnSourcePlan {
  const allowed = new Set<IngestionColumn>(ingestionColumns);
  const allowedTiers = new Set<SourceTier>(sourceTiers);
  const tiers = requestedTiers
    ? [...new Set(requestedTiers.filter((tier): tier is SourceTier => allowedTiers.has(tier as SourceTier)))]
    : undefined;
  const columns = requestedColumns
    ? [...new Set(requestedColumns.filter((column): column is IngestionColumn => allowed.has(column as IngestionColumn)))]
    : [...ingestionColumns];
  return {
    columns,
    sources: columns.flatMap((column) => getEnabledSourcesForColumn(column, tiers)),
  };
}

export function shouldKeepCandidateForColumn(column: IngestionColumn, candidateText: string): boolean {
  const policy = columnIngestionPolicies[column];
  const text = candidateText.toLowerCase();
  const hasInclude = policy.include_keywords.some((keyword) => keywordMatches(text, keyword));
  const hasExclude = policy.exclude_keywords.some((keyword) => keywordMatches(text, keyword));
  return hasInclude && !hasExclude;
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function keywordMatches(normalizedText: string, keyword: string): boolean {
  const normalizedKeyword = keyword.toLowerCase().trim();
  if (!normalizedKeyword) return false;

  // Short tokens such as "AI" and "LLM" must not match inside unrelated words
  // like "laid" or "allmeta". Longer phrases can safely use substring matching.
  if (/^[a-z0-9+#.-]{1,4}$/.test(normalizedKeyword)) {
    return new RegExp(`(^|[^a-z0-9])${escapeRegExp(normalizedKeyword)}([^a-z0-9]|$)`, "i").test(normalizedText);
  }

  return normalizedText.includes(normalizedKeyword);
}

export function buildCandidateFilterText(
  sourceName: string,
  row: { title?: string; summary?: string; source_text?: string; tags?: string[] },
): string {
  const sourceLinePattern =
    /^(feed source|source|web index source|index title|code search source|paper source|course source|product discovery source|discovery source):.*$/gim;
  const cleanedSourceText = (row.source_text ?? "")
    .replace(sourceLinePattern, " ")
    .replaceAll(sourceName, " ");
  return [row.title, row.summary, cleanedSourceText, (row.tags ?? []).join(" ")]
    .filter(Boolean)
    .join("\n")
    .replace(/\s+/g, " ")
    .trim();
}

export function getCandidateFetchLimitForSource(source: ColumnSourceConfig, requestedColumnLimit?: number): number {
  const sourceTarget = Math.max(1, source.selected_limit ?? requestedColumnLimit ?? 1);
  const columnTarget = requestedColumnLimit && requestedColumnLimit > 0 ? Math.max(1, requestedColumnLimit) : sourceTarget;
  const target = Math.min(sourceTarget, columnTarget);
  return Math.min(source.raw_limit ?? target, 30, Math.max(target * 4, target));
}
