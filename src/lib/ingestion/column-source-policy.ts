function withPolicy(
  source: Omit<ColumnSourceConfig, "raw_limit" | "selected_limit" | "cadence" | "output_goal" | "include_keywords" | "exclude_keywords" | "source_tier"> &
    Partial<Pick<ColumnSourceConfig, "raw_limit" | "selected_limit" | "cadence" | "output_goal" | "include_keywords" | "exclude_keywords" | "source_tier">>,
): ColumnSourceConfig {
  const policy = columnIngestionPolicies[source.column];
  return {
    ...source,
    raw_limit: source.raw_limit ?? policy.raw_limit,
    selected_limit: source.selected_limit ?? policy.selected_limit,
    cadence: source.cadence ?? policy.cadence,
    output_goal: source.output_goal ?? policy.output_goal,
    include_keywords: source.include_keywords ?? policy.include_keywords,
    exclude_keywords: source.exclude_keywords ?? policy.exclude_keywords,
    source_tier: source.source_tier ?? inferSourceTier(source),
  };
}

function inferSourceTier(source: Pick<ColumnSourceConfig, "column" | "role" | "source_type">): SourceTier {
  if (source.role === "benchmark") return "benchmark_data";
  if (source.role === "official") return "official_primary";
  if (source.column === "models" && source.source_type === "official") return "official_primary";
  if (source.column === "skills" || source.column === "courses") return "weekly_deep";
  if (source.column === "articles") return source.role === "discovery_signal" ? "deep_enrichment" : "weekly_deep";
  return "core_discovery";
}

